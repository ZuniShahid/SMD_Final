package com.example.final_smd;

public class HWS {
    public double av;
    public int ct;
    public double mn;
    public double mx;

    public double getAv() {
        return av;
    }

    public void setAv(double av) {
        this.av = av;
    }

    public int getCt() {
        return ct;
    }

    public void setCt(int ct) {
        this.ct = ct;
    }

    public double getMn() {
        return mn;
    }

    public void setMn(double mn) {
        this.mn = mn;
    }

    public double getMx() {
        return mx;
    }

    public void setMx(double mx) {
        this.mx = mx;
    }
}
